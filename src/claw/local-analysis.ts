/**
 * Local Analysis — On-device legal review for confidential documents.
 *
 * v2 (2026-04-27): clause-aware map-reduce analysis.
 *
 * The previous version sent the first 8 K characters of a document to a
 * single LLM call and asked the model both to (a) extract verbatim quotes
 * and (b) reason about every clause. Small models (Gemma 2 2B, Phi-3 3B,
 * Llama 3.2 3B) cut both corners: they emitted "..." quote placeholders,
 * tagged everything "info" severity, and produced generic boilerplate
 * concerns that could apply to any contract.
 *
 * v2 fixes the design so a 2-3B model can produce useful triage:
 *
 *   1. **Clause-boundary chunking.** We segment the document by
 *      matching `^N. <Title>` patterns, producing one chunk per clause
 *      (≈21 chunks for a typical JV agreement). The model sees full
 *      clause text, not a 18% truncation.
 *
 *   2. **Pre-extracted text.** The chunker pulls the verbatim text
 *      from the document and hands it to the model. The model never
 *      has to "quote" — it only analyses what it was given. Kills
 *      the "..." placeholder failure.
 *
 *   3. **Per-clause prompts.** One LLM call per clause keeps each
 *      input small and focused. Small models excel at narrow tasks.
 *
 *   4. **Force-structured concerns.** Each concern must reference a
 *      specific party / defined term / dollar amount or it's stripped.
 *
 *   5. **Severity budget.** A final synthesis pass re-allocates
 *      severities — the model can't escape into "everything is info".
 *
 *   6. **Synthesis pass.** A final LLM call merges per-clause findings
 *      into top-N risks, executive summary, and recommendations.
 *
 * Cost: $0 per document (local inference).
 */

